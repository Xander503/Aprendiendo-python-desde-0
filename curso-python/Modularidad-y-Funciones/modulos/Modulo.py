# definimos una funcion saludo
def Saludo(nombre):
    print(f"hola {nombre}")


# definimos una funcion para calcular el area de un cuadrado
def AreaCuadrado(lado):
    return print(lado**2)


# definimos una funcion para calcular el area de un rectangulo
def AreaRectangulo(base, altura):
    return print(base * altura)


# definimos una funcion para calcular el area de un triangulo
def AreaTriangulo(base, altura):
    return print((base * altura) / 2)
